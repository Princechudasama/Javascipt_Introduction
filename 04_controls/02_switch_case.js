//switch
const month = 12;
switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("Februar");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;
    default:
        console.log("it is above 5")
        break;
}